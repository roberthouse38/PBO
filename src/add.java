public class add {

        public static void main(String[] args) {
            int jumlah = 0; // Variabel untuk menyimpan hasil jumlah

            // Menggunakan for loop untuk menjumlahkan bilangan dari 1       hingga 10
            for (int i = 1; i <= 10; i++) {
                jumlah += i; // Menambahkan nilai i ke variabel jumlah
            }

            // Menampilkan hasil penjumlahan
            System.out.println("Jumlah bilangan dari 1 hingga 10 adalah: " + jumlah);
        }
    }

